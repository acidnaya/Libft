#include <string.h>

void	*ft_memcpy(void *dst, const void *src, size_t num)
{
	size_t			i;
	unsigned char	*dst1;
	unsigned char	*src1;

	dst1 = (unsigned char *)dst;
	src1 = (unsigned char *)src;
	i = 0;
	while (i < num)
	{
		dst1[i] = src1[i];
		i++;
	}
	return (dst1);
}
